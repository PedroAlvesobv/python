import math
from math import sqrt, floor
num = int(input('Digite um número: '))
raiz = math.sqrt(num)
print(f'A raiz de {num} é {raiz:.2f}') #Para mostrar com 2 casas decimais

#print(f'A raiz de {num} é {math.ceil(raiz)}')  para arredondar a unidades
#print(f'A raiz de {num} é {math.floor(raiz)}') para arredondar para baixo nas dezenas